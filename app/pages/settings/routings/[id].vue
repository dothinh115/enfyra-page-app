<template>
  <div class="space-y-6">
    <!-- Content - Limited width -->
    <div class="max-w-[1000px] lg:max-w-[1000px] md:w-full space-y-6">
      <CommonFormCard>
        <UForm :state="form" @submit="updateRoute">
          <FormEditorLazy
            ref="formEditorRef"
            v-model="form"
            v-model:errors="errors"
            @has-changed="(hasChanged) => hasFormChanges = hasChanged"
            :table-name="tableName"
            :excluded="['routePermissions', 'mainTable']"
            :type-map="typeMap"
            :loading="loading"
          />
        </UForm>
      </CommonFormCard>

      <!-- Route Permissions Section -->
      <CommonFormCard>
        <PermissionManager
          table-name="route_permission_definition"
          :current-field-id="{ field: 'route', value: route.params.id as string }"
          icon="lucide:shield"
          title="Route Permissions"
        />
      </CommonFormCard>
    </div>
  </div>

  <CommonEmptyState
    v-if="!loading && !routeData?.data?.[0]"
    title="Route not found"
    description="The requested route could not be loaded"
    icon="lucide:route"
    size="sm"
  />
</template>

<script setup lang="ts">
const route = useRoute();
const toast = useToast();
const { confirm } = useConfirm();
const { loadRoutes } = useRoutes();

const tableName = "route_definition";
const hasFormChanges = ref(false);
const formEditorRef = ref();

const { validate, getIncludeFields } = useSchema(tableName);
const { registerPageHeader } = usePageHeaderRegistry();

// Register page header with dynamic title
watch(() => routeData.value?.data?.[0]?.path, (path) => {
  if (path) {
    registerPageHeader({
      title: `Route: ${path}`,
      gradient: "cyan",
    });
  }
}, { immediate: true });

// Check if route has associated table to disable isEnabled field
const hasAssociatedTable = computed(() => {
  const currentRoute = routeData.value?.data?.[0];
  if (!currentRoute?.mainTable) return false;

  const { schemas } = useSchema();
  const { getId } = useDatabase();
  const mainTableId = getId(currentRoute.mainTable);
  if (!mainTableId) return false;

  return Object.values(schemas.value).some(
    (table: any) => String(getId(table)) === String(mainTableId)
  );
});

// Type map to disable isEnabled field if route has associated table
const typeMap = computed(() => ({
  isEnabled: {
    disabled: hasAssociatedTable.value
  }
}));

useHeaderActionRegistry([
  {
    id: "save-routing",
    label: "Save",
    icon: "lucide:save",
    variant: "solid",
    color: "primary",
    size: "md",
    submit: updateRoute,
    loading: computed(() => updateLoading.value),
    disabled: computed(() => !hasFormChanges.value),
    permission: {
      and: [
        {
          route: "/route_definition",
          actions: ["update"],
        },
      ],
    },
  },
  {
    id: "delete-routing",
    label: "Delete",
    icon: "lucide:trash",
    variant: "solid",
    color: "error",
    size: "md",
    onClick: deleteRoute,
    loading: computed(() => deleteLoading.value),
    permission: {
      and: [
        {
          route: "/route_definition",
          actions: ["delete"],
        },
      ],
    },
  },
]);

const {
  data: routeData,
  pending: loading,
  execute: executeGetRoute,
} = useApi(`/${tableName}`, {
  query: {
    fields: getIncludeFields(),
    filter: { id: { _eq: route.params.id } },
  },
  errorContext: "Fetch Route",
});

const {
  error: updateError,
  execute: executeUpdateRoute,
  pending: updateLoading,
} = useApi(`/${tableName}`, {
  method: "patch",
  errorContext: "Update Route",
});

const {
  error: deleteError,
  execute: executeDeleteRoute,
  pending: deleteLoading,
} = useApi(`/${tableName}`, {
  method: "delete",
  errorContext: "Delete Route",
});

const form = ref<Record<string, any>>({});

const errors = ref<Record<string, string>>({});

async function initializeForm() {
  await executeGetRoute();
  const data = routeData.value?.data?.[0];
  if (data) {
    form.value = { ...data };
  }
}

async function updateRoute() {
  if (!form.value) return;

  const { isValid, errors: validationErrors } = validate(form.value);
  if (!isValid) {
    errors.value = validationErrors;
    toast.add({
      title: "Missing information",
      description: "Please fill in all required fields.",
      color: "error",
    });
    return;
  }

  await executeUpdateRoute({
    id: route.params.id as string,
    body: form.value,
  });

  if (updateError.value) {
    return;
  }

  toast.add({
    title: "Success",
    color: "success",
    description: "Route updated!",
  });
  errors.value = {};

  await loadRoutes();

  // Reregister menus after route update
  const { registerTableMenusWithSidebarIds } = useMenuRegistry();
  const { schemas } = useSchema();
  await registerTableMenusWithSidebarIds(Object.values(schemas.value));

  // Confirm form changes as new baseline
  formEditorRef.value?.confirmChanges();
}

async function deleteRoute() {
  const ok = await confirm({
    title: "Are you sure?",
    content: "This action cannot be undone.",
  });
  if (!ok) return;

  await executeDeleteRoute({ id: route.params.id as string });

  if (deleteError.value) {
    return;
  }

  toast.add({ 
    title: "Success",
    description: "Route deleted successfully", 
    color: "success" 
  });

  await loadRoutes();

  // Reregister menus after route deletion
  const { registerTableMenusWithSidebarIds } = useMenuRegistry();
  const { schemas } = useSchema();
  await registerTableMenusWithSidebarIds(Object.values(schemas.value));
  
  await navigateTo("/settings/routings");
}

onMounted(() => {
  initializeForm();
});
</script>
