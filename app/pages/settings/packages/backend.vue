<template>
  <div class="space-y-6">
    <!-- Header -->
    <CommonPageHeader
      title="Backend Packages"
      title-size="md"
      show-background
      background-gradient="from-emerald-500/8 via-green-400/5 to-transparent"
      padding-y="py-6"
    />

    <!-- Packages List -->
    <Transition name="loading-fade" mode="out-in">
      <CommonLoadingState
        v-if="!isMounted || loading"
        title="Loading packages..."
        description="Fetching installed backend packages"
        size="sm"
        type="card"
        context="page"
      />

      <div
        v-else-if="packages.length > 0"
        class="grid gap-4"
        :class="
          isTablet ? 'grid-cols-2' : 'grid-cols-1 md:grid-cols-2 lg:grid-cols-3'
        "
      >
        <CommonSettingsCard
          v-for="pkg in packages"
          :key="pkg.id"
          :title="pkg.name"
          :description="pkg.description || 'No description'"
          icon="lucide:server"
          icon-color="success"
          :card-class="'cursor-pointer lg:hover:ring-2 lg:hover:ring-success/20 transition-all'"
          @click="navigateTo(`/settings/packages/${pkg.id}`)"
          :stats="[
            {
              label: 'Version',
              component: 'UBadge',
              props: {
                variant: 'soft',
                color: 'success',
              },
              value: pkg.version,
            },
            {
              label: 'Installed',
              value: new Date(pkg.createdAt).toLocaleDateString(),
            },
            ...(pkg.flags
              ? [
                  {
                    label: 'Flags',
                    value: pkg.flags,
                  },
                ]
              : []),
            {
              label: 'Usage',
              value: `$ctx.$pkgs.${pkg.name.replace(/[@\/\-]/g, '')}`,
              component: 'code',
              props: {
                class:
                  'text-xs font-mono text-green-600 bg-green-50 px-2 py-1 rounded',
              },
            },
          ]"
          :actions="[]"
          :header-actions="getHeaderActions(pkg)"
        />
      </div>

      <CommonEmptyState
        v-else
        title="No backend packages installed"
        description="Install packages to enhance your handlers and hooks"
        icon="lucide:server"
        size="sm"
      />
    </Transition>

    <!-- Pagination -->
    <div
      class="flex justify-center mt-4"
      v-if="!loading && packages.length > 0"
    >
      <UPagination
        v-if="total > limit"
        v-model:page="page"
        :items-per-page="limit"
        :total="total"
        show-edges
        :sibling-count="1"
        :to="
          (p) => ({
            path: route.path,
            query: { ...route.query, page: p },
          })
        "
        color="secondary"
        active-color="secondary"
      />
    </div>
  </div>
</template>

<script setup lang="ts">
// useApi is auto-imported in Nuxt
const page = ref(1);
const limit = 9;
const { confirm } = useConfirm();
const toast = useToast();
const route = useRoute();
const { isTablet } = useScreen();
const { isMounted } = useMounted();

// Header actions
useHeaderActionRegistry({
  id: "create-backend-package",
  label: "Install Package",
  icon: "lucide:server",
  variant: "solid",
  color: "primary",
  size: "md",
  to: "/settings/packages/install",
  permission: {
    and: [
      {
        route: "/package_definition",
        actions: ["create"],
      },
    ],
  },
});

const {
  data: apiData,
  pending: loading,
  execute: loadPackages,
} = useApi("/package_definition", {
  query: computed(() => ({
    page: page.value,
    limit,
    filter: {
      type: { _eq: "Backend" },
    },
  })),
  errorContext: "Load Backend Packages",
});

const packages = computed(() => apiData.value?.data || []);
const total = computed(() => apiData.value?.meta?.totalCount || 0);

// Delete API setup  
const { execute: removePackage, error: removePackageError } = useApi(
  "/package_definition",
  {
    method: "delete",
    errorContext: "Uninstall Package",
  }
);

const deletingPackages = ref(new Set<string>());

// Delete package
async function deletePackage(pkg: any) {
  const isConfirmed = await confirm({
    title: "Uninstall Package",
    content: `Are you sure you want to uninstall "${pkg.name}"? This action cannot be undone.`,
    confirmText: "Uninstall",
    cancelText: "Cancel",
  });

  if (!isConfirmed) return;

  deletingPackages.value.add(pkg.id);

  try {
    await removePackage({ id: pkg.id });

    if (removePackageError.value) {
      return;
    }

    toast.add({
      title: "Success",
      description: `Package ${pkg.name} uninstalled successfully`,
      color: "success",
    });

    await loadPackages();
  } finally {
    deletingPackages.value.delete(pkg.id);
  }
}

// Get header actions for each package
function getHeaderActions(pkg: any) {
  return [
    {
      component: "UButton",
      props: {
        icon: "i-heroicons-trash",
        variant: "outline",
        color: "error",
        loading: deletingPackages.value.has(pkg.id),
      },
      onClick: (e?: Event) => {
        e?.stopPropagation();
        deletePackage(pkg);
      },
    },
  ];
}

watch(
  page,
  () => {
    loadPackages();
  },
  { immediate: true }
);

</script>
